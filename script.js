// Use Arithmetic Operators:
const additionOperator = 5 + 4;
console.log(`5 + 4 = ${additionOperator}`);

const subtractionOperator = 10 - 8;
console.log(`10 - 8 = ${subtractionOperator}`);

const multiplicationOperator = 27 * 6;
console.log(`27 * 6 = ${multiplicationOperator}`);

const divisionOperator = 36 / 6;
console.log(`36 / 6 = ${divisionOperator}`);

// Use Comparison Operators:
const greaterThanOperator = 20 > 5;
console.log(`20 > 5: ${greaterThanOperator}`);

const lessThanOperator = 18 < 100;
console.log(`18 < 100: ${lessThanOperator}`);

const equalToOperator = '20' === 20;
console.log(`'20' === 20: ${equalToOperator}`);

const notEqualToOperator = 'banana' !== 'apple';
console.log(`'banana' !== 'apple': ${notEqualToOperator}`);

// Use Logical Operators:
const andOperator = 20 > 10 && '100' == 100;
console.log(`20 > 10 && '100' == 100: ${andOperator}`);

const orOperator = true || false;
console.log(`true || false: ${orOperator}`);

const notOperator = 1000 != 100;
console.log(`1000 != 100: ${notOperator}`);
